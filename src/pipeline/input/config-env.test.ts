/**
 * ConfigEnv の読み込み挙動を検証するテストスイート。
 * 仕様確認のためのテストケースを先に宣言しておき、TDD のフェーズに沿って実装する。
 */
describe("ConfigEnv", () => {
  it("ベースの .env が存在しない場合は空の環境を構築する");

  it("ベースの .env からキーと値を読み込む");

  it("suffix が指定され存在する場合は .env.{suffix} の値で上書きする");

  it("suffix が指定されていても .env.{suffix} が存在しない場合はベースの値を維持する");

  it("baseDir オプションで探索ディレクトリを切り替えられる");

  it("get/has/entries で保持している値へアクセスできる");
});
